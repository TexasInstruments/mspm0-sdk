#include "ti_msp_dl_config.h"

/* Delay for 5ms to ensure UART TX is idle before starting transmission */
#define UART_TX_DELAY (160000)

uint8_t txData = 0, rxData = 0;
bool dataReceived;

int main(void)
{
    SYSCFG_DL_init();

    NVIC_ClearPendingIRQ(UART_0_INST_INT_IRQN);
    NVIC_EnableIRQ(UART_0_INST_INT_IRQN);

    /* Optional delay to ensure UART TX is idle before starting transmission */
    delay_cycles(UART_TX_DELAY);

    while (1) {
        dataReceived = false;
        txData       = txData + 1;
        DL_UART_Main_transmitData(UART_0_INST, txData);

        while (dataReceived == false) {
            __WFE();
        }
        delay_cycles(10000);
    }
}

void UART_0_INST_IRQHandler(void)
{
    switch (DL_UART_Main_getPendingInterrupt(UART_0_INST)) {
        case DL_UART_MAIN_IIDX_RX:
            rxData       = DL_UART_Main_receiveDataBlocking(UART_0_INST);
            dataReceived = true;
            while (rxData != txData) {
            };
            break;
        default:
            break;
    }
}
